// Generate all countries from libphonenumber-js
export function generateAllCountries(): Country[] {
  const allCountryCodes = getCountries();
  
  return allCountryCodes.map((code, index) => {
    const dialCode = `+${getCountryCallingCode(code)}`;
    const name = COUNTRY_NAMES[code] || code;
    const flag = COUNTRY_FLAGS[code] || 'üè≥Ô∏è';
    const priority = PRIORITY_COUNTRIES.includes(code) ? PRIORITY_COUNTRIES.indexOf(code) + 1 : undefined;
    
    return {
      code,
      name,
      dialCode,
      flag,
      format: '###########', // Generic format, can be improved later
      maxLength: 15, // Max international phone number length
      priority
    };
  }).sort((a, b) => {
    // Sort by priority first, then alphabetically
    if (a.priority && b.priority) {
      return a.priority - b.priority;
    }
    if (a.priority && !b.priority) return -1;
    if (!a.priority && b.priority) return 1;
    return a.name.localeCompare(b.name);
  });
}

